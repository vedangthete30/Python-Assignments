def Addition(num1, num2):
    ans = num1 + num2
    print("Addition of given numbers : ", ans)

def Subtraction(num1, num2):
    ans = num1 - num2
    print("Subtraction of given numbers : ", ans)

def Multiplication(num1, num2):
    ans = num1 * num2
    print("Multiplication of given numbers : ", ans)

def Division(num1, num2):
    ans = num1 / num2
    print("Division of given numbers : ", ans)